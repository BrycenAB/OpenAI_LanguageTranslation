def find_closest_match(input_string):
    closest_match = None
    closest_ratio = 0
    for key in languages:
        ratio = difflib.SequenceMatcher(None, input_string, key).ratio()  # Calculate the similarity ratio
        if ratio > closest_ratio:
            closest_ratio = ratio
            closest_match = key
    return closest_match


